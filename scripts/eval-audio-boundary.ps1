function Invoke-MulticutFixture {
    $manifestPath = Join-Path $FixturesRoot 'multicut_01.manifest.json'
    if (-not (Test-Path $manifestPath)) {
        throw "multicut_01 manifest missing — run scripts/generate-boundary-fixtures.ps1"
    }
    $raw = Get-Content $manifestPath -Raw | ConvertFrom-Json
    $M = @{
        sample_rate   = [int]$raw.sample_rate
        export        = [string]$raw.export
        preview       = [string]$raw.preview
        seams_in_edit = @($raw.seams_in_edit | ForEach-Object { @{ index = [int]$_.index; sample = [int]$_.sample } })
        keep_segments = @($raw.keep_segments | ForEach-Object { @{ start = [double]$_.start; end = [double]$_.end } })
    }
    $results = @()
    # Only E2 (seam quality), E3 (parity), E5 (sample boundary) apply to this
    # fixture — no deleted word, no transcript. Mark E1/E4 skip.
    $results += New-Result 'E1_leak_xcorr'     'skip' @{} 'multicut fixture has no deleted-word stem'
    $results += Invoke-E2-SeamZScore    -M $M -EditWav $M.export
    $results += Invoke-E3-PreviewParity -M $M -PreviewWav $M.preview -ExportWav $M.export
    $results += New-Result 'E4_transcript_wer' 'skip' @{} 'multicut fixture has no reference transcript'
    $results += Invoke-E5-SampleBoundary -M $M -EditWav $M.export
    return @{ name = 'multicut_01'; variant = 'default'; results = $results }
}

# --- Driver ---------------------------------------------------------------
Assert-FFmpeg

$fixtures = @()
if ([string]::IsNullOrWhiteSpace($Fixture) -or $Fixture -eq 'phrase_01') {
    $fixtures += (Invoke-PhraseFixture)
}
if ([string]::IsNullOrWhiteSpace($Fixture) -or $Fixture -eq 'multicut_01') {
    $fixtures += (Invoke-MulticutFixture)
}
if ([string]::IsNullOrWhiteSpace($Fixture) -or $Fixture -eq 'overlap_01') {
    $fixtures += (Invoke-OverlapFixture)
}
if ($fixtures.Count -eq 0) {
    Write-Host "No fixture matched '$Fixture'. Available: phrase_01, multicut_01, overlap_01" -ForegroundColor Red
    exit 2
}

$stamp = Get-Date -Format 'yyyyMMdd-HHmmss'
$overallStatus = 'pass'
$fixtureReports = @()

foreach ($fx in $fixtures) {
    $fxDir = Join-Path $OutputRoot "$($fx.name)\$stamp"
    New-Item -ItemType Directory -Force -Path $fxDir | Out-Null

    $failed  = @($fx.results | Where-Object { $_.status -eq 'fail'  })
    $errored = @($fx.results | Where-Object { $_.status -eq 'error' })
    $fxStatus = if ($errored.Count) { 'error' } elseif ($failed.Count) { 'fail' } else { 'pass' }
    if ($fxStatus -ne 'pass') { $overallStatus = 'fail' }

    $fxReport = [ordered]@{
        timestamp    = (Get-Date).ToUniversalTime().ToString('yyyy-MM-ddTHH:mm:ssZ')
        fixture      = $fx.name
        variant      = $fx.variant
        status       = $fxStatus
        sample_rate  = $SampleRate
        thresholds   = $Th
        evals        = $fx.results
    }
    $jsonPath = Join-Path $fxDir 'report.json'
    $mdPath   = Join-Path $fxDir 'report.md'
    ($fxReport | ConvertTo-Json -Depth 10) | Set-Content -Path $jsonPath -Encoding UTF8

    $md = @()
    $md += "# audio-boundary eval: $($fx.name) ($($fx.variant))"
    $md += ""
    $md += "Status: **$fxStatus**"
    $md += ""
    $md += "| Eval | Status | Key metric | Threshold |"
    $md += "| --- | --- | --- | --- |"
    foreach ($r in $fx.results) {
        $metric = ($r.metrics.GetEnumerator() | Select-Object -First 1)
        $metricStr = if ($metric) { "$($metric.Key)=$($metric.Value)" } else { '-' }
        $md += "| $($r.name) | $($r.status) | $metricStr | see JSON |"
    }
    $md += ""
    foreach ($r in $fx.results) {
        $md += "## $($r.name) — $($r.status)"
        if ($r.notes) { $md += $r.notes; $md += "" }
        $md += '```json'
        $md += ($r.metrics | ConvertTo-Json -Depth 6)
        $md += '```'
        $md += ""
    }
    ($md -join "`n") | Set-Content -Path $mdPath -Encoding UTF8

    $fixtureReports += @{ fixture = $fx.name; status = $fxStatus; json = $jsonPath; md = $mdPath; results = $fx.results }
}

# Console summary
Write-Host ""
Write-Host "=== audio-boundary eval summary ===" -ForegroundColor Cyan
foreach ($fr in $fixtureReports) {
    $color = if ($fr.status -eq 'pass') { 'Green' } elseif ($fr.status -eq 'fail') { 'Red' } else { 'Yellow' }
    Write-Host ("  [{0}] {1}" -f $fr.status.ToUpper(), $fr.fixture) -ForegroundColor $color
    foreach ($r in $fr.results) {
        $c = if ($r.status -eq 'pass') { 'Green' } elseif ($r.status -eq 'fail') { 'Red' } else { 'DarkYellow' }
        Write-Host ("      - {0,-22} {1}" -f $r.name, $r.status) -ForegroundColor $c
        if ($r.status -ne 'pass' -and $r.notes) { Write-Host "          $($r.notes)" -ForegroundColor DarkGray }
    }
    Write-Host ("      report: {0}" -f $fr.json) -ForegroundColor DarkGray
}
Write-Host ""
$overallColor = if ($overallStatus -eq 'pass') { 'Green' } else { 'Red' }
Write-Host ("Overall: {0}" -f $overallStatus.ToUpper()) -ForegroundColor $overallColor

if ($overallStatus -ne 'pass') { exit 1 }
exit 0



