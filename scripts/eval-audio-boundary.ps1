        source            = [string]$raw.source
        clean_edit        = [string]$raw.clean_edit
        leaky_edit        = [string]$raw.leaky_edit
        preview           = [string]$raw.preview
        deleted_word_stem = [string]$raw.deleted_word_stem
        seams_in_edit     = @($raw.seams_in_edit | ForEach-Object { @{ index = [int]$_.index; sample = [int]$_.sample } })
        keep_segments     = @($raw.keep_segments | ForEach-Object { @{ start = [double]$_.start; end = [double]$_.end } })
    }
    $editWav = if ($ForceLeaky.IsPresent) { $M.leaky_edit } else { $M.clean_edit }
    $results = @()
    try { $results += Invoke-E1-LeakXcorr     -M $M -EditWav $editWav } catch { Write-Host "E1 FAIL: $($_.Exception.Message)" -ForegroundColor Red; $results += (New-Result 'E1_leak_xcorr' 'error' @{} $_.Exception.Message) }
